    public void registrarCliente(){
        Cliente c = new Cliente(
                txtDni.getText(),
                txtNombre.getText(),
                txtApellido.getText(),
                txtCorreo.getText(),
                txtTelefono.getText(),
                new String (txtContra.getPassword())
        );
        
        cliente.registrarCliente(c);
        JOptionPane.showMessageDialog(this, "Cliente registrado exitosamente.");
        
    }

    public void buscarCliente() {
        try {
            int id = Integer.parseInt(txtDni.getText()); // Suponemos que el ID del cliente est√° en el campo "DNI"
            Cliente c = cliente.buscarCliente(id); // Llamada al m√©todo del controlador

            if (c != null) {
                // Mostrar datos en los campos
                txtNombre.setText(c.getNombre());
                txtApellido.setText(c.getApellido());
                txtCorreo.setText(c.getCorreo());
                txtTelefono.setText(c.getTelefono());
                txtContra.setText(c.getContrasena());
                JOptionPane.showMessageDialog(this, "Cliente encontrado.");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ un cliente con el ID especificado.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un n√∫mero v√°lido.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al buscar cliente: " + ex.getMessage());
        }
    }


